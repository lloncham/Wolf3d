#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int i;

	i = 0;
	while (n != 0)
	{
		((char *)s)[i] = (unsigned char)c;
		n--;
		i++;
	}
	return ((char *)s);
}
